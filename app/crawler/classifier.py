"""
极简关键词分类器
v1 用规则分类，v2 再考虑模型微调（保持申报书叙事不冲突）。

分类标签：学业 / 活动 / 党团 / 就业 / 其他
"""

# 关键词按权重从高到低排列；先命中先赢
CATEGORY_KEYWORDS: dict[str, list[str]] = {
    "就业": [
        "招聘", "实习", "宣讲会", "校招", "求职", "简历",
        "应聘", "offer", "用人单位", "就业", "招录",
    ],
    "党团": [
        "党支部", "团支部", "党课", "团课", "党员", "团员",
        "组织生活", "主题教育", "党建", "团委", "学生会", "学生干部",
        "团日活动", "党日活动",
    ],
    "学业": [
        "选课", "考试", "成绩", "学位", "教务", "学分",
        "毕业", "保研", "考研", "学籍", "课程", "课表",
        "答辩", "开题", "中期", "推免", "重修",
    ],
    "活动": [
        "讲座", "报告会", "沙龙", "比赛", "赛事", "邀请",
        "嘉宾", "盛典", "展览", "招生简章", "工作坊", "论坛",
        "颁奖", "巡展", "公开课", "招新",
    ],
}


def classify(title: str, content: str = "") -> str:
    """根据标题 + 正文前 200 字做关键词匹配"""
    text = title + " " + content[:200]
    for category, keywords in CATEGORY_KEYWORDS.items():
        for kw in keywords:
            if kw in text:
                return category
    return "其他"
